function mostrarTexto(p1) {
    var pElement1 = document.getElementById("p1");
    var btnPlus1 = document.getElementById("btnPlus1");
    var btnMinus1 = document.getElementById("btnMinus1");

    if (pElement1.style.display === 'block') {
        pElement1.style.display = 'none';
        btnMinus1.style.display = 'none';
        btnPlus1.style.display = 'block'
    } else {
        pElement1.style.display = 'block';
        btnMinus1.style.display = 'block';
        btnPlus1.style.display = 'none';
    }
}

